
package bs23.com.dragsite.model;

public class Location {

    private float lat;
    private float lng;

    /**
     * 
     * @return
     *     The lat
     */
    public float getLat() {
        return lat;
    }

    /**
     * 
     * @param lat
     *     The lat
     */
    public void setLat(float lat) {
        this.lat = lat;
    }

    /**
     * 
     * @return
     *     The lng
     */
    public float getLng() {
        return lng;
    }

    /**
     * 
     * @param lng
     *     The lng
     */
    public void setLng(float lng) {
        this.lng = lng;
    }

}
